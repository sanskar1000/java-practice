class DatatypesDemo {
    public static void main(String[] args) {

   //How to use datatype
   int a = 10;
   double b = 12.3;
   char c = 'a';  
   boolean d = true;   
   
   System.out.println("Its a int datatype. It store integer value: "+a);
   System.out.println("Its a double datatype. It store decimial value: "+b);
   System.out.println("Its a character datatype. It store character : "+c);
   System.out.println("Its a  boolean datatype. It stores in true or false  : "+d);

    }
}
